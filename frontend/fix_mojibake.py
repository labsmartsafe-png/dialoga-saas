total_fixed = 0

for f in files:
    path = os.path.join(base, f)
    if not os.path.exists(path):
        print("-- " + f + " (nao existe)")
        continue
    with open(path, "rb") as fp:
        raw = fp.read()
    original = raw
    for emoji_bytes, entity in EMOJI_BYTES.items():
        raw = raw.replace(emoji_bytes, entity.encode("utf-8"))
    if raw != original:
        with open(path, "wb") as fp:
            fp.write(raw)
        total_fixed += 1
        diff = len(original) - len(raw)
        print("OK " + f + " (" + str(diff) + " bytes)")
    else:
        print("-- " + f)

